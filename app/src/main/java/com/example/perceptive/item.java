package com.example.perceptive;

public class item {
    String userid;
    String name;
    String desc;
    String ext;
    String imgurl;
    String type;
    String option;

    item(String userid,String name,String desc,String ext,String imgurl,String type,String option){
        this.userid = userid;
        this.name = name;
        this.desc = desc;
        this.ext = ext;
        this.imgurl = imgurl;
        this.type = type;
        this.option = option;
    }
}
